package co.shop.social.service;

public class SocialVO {
	private String socialId;
	private String socialName;
	private String socialAddr;
	private String socialTel;
	private String socialGrade;
	public String getSocialId() {
		return socialId;
	}
	public void setSocialId(String socialId) {
		this.socialId = socialId;
	}
	public String getSocialName() {
		return socialName;
	}
	public void setSocialName(String socialName) {
		this.socialName = socialName;
	}
	public String getSocialAddr() {
		return socialAddr;
	}
	public void setSocialAddr(String socialAddr) {
		this.socialAddr = socialAddr;
	}
	public String getSocialTel() {
		return socialTel;
	}
	public void setSocialTel(String socialTel) {
		this.socialTel = socialTel;
	}
	public String getSocialGrade() {
		return socialGrade;
	}
	public void setSocialGrade(String socialGrade) {
		this.socialGrade = socialGrade;
	}
	
	
}
